    <div role="tabpanel" class="tab-pane fade" id="messages">
        <h2>小学帮思维导图 百日经典诵读活动</h2>
        <h3>一、活动主题：</h3>
        培养创新思维，弘扬传统文化

       <h3>二、活动时间：</h3>
        2018 年3 月20 日---6 月30 日

       <h3>三、参加活动对象：</h3>
        7-15 岁学生或家长

       <h3>四、活动背景：</h3>
        2018 年国家“两会”提出大力弘扬传统文化，教育部发文反复强调经典名篇对中小学生教育的重要性。近日，国家新闻出版广电总局发布开展2018 年全民阅读工作通知：全民阅读工作要深入贯彻落实习近平新时代中国特色社会主义思想和党的十九大精神，创新思路、创新方法、创新举措，通过全年化、常态化、制度化，体系化、工程化、项目化，加快构建全民阅读推广服务体系，扎实推动新时代全民阅读工作更加快速、更加深入、更加全面、更加科学发展。
        小学帮响应国家号召，秉承“培养创新思维，弘扬传统文化”的教育理念，为满足小学生对优秀传统文化的渴求，特组织教育领域优秀教师和古文学专家策划了“小学帮思维导图百日经典诵读”活动。

       <h3>五、活动特色：</h3>
        1. 思维导图深度学习经典名篇；
        2. 选材贴近小学生生活，提高语文素养，培养审美情趣；
        3. 老师点评，给予指导；
        4. 坚持100 天，颁发“中华小小朗读者”证书（港粤澳语文教育研究院颁发），并免费获得精美的《小学作文》杂志一本。

      <h3>六、活动形式：</h3>
        1. 建立诵读微信群，参与者加入微信群；
        2. 每日微信群诵读（也可使用第三方软件录音）；
        3. 名师点评、互相激励。

      <h3>七、活动意义：</h3>
        1、培养创新思维，弘扬传统文化
        2、通过参与此活动，让参与者轻松、快捷地掌握一种有效的学习工具——思维导图。
        3、通过该活动的开展，让更多的家庭、孩子享受中华传统文化带来的欢乐和兴趣，让孩子树立学习的信心。
        4、通过家长和孩子共同参与学习，增强亲子关系，培养学生良好的阅读习惯、学习能力、创新思维。
        5、通过该活动过程中“经典诵读、思维导图学习”，促使孩子更好地成长。
        6、通过举办全国性的统一活动，为总部经常性地开展统一活动提供依据和积累经验。
